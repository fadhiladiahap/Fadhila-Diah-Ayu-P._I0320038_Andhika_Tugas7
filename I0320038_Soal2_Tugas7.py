# Fungsi floor
import math
nilai = 82.7
print ("Fadhila mendapatkan nilai kalkulus sebesar", math.floor(nilai), "pada ujian tengah semester 1")

# Fungsi min dan max
import math
AEB = 96
PRD = 97
Psikin = 98
print ("Nilai tertinggi yang Fadhila dapatkan pada semester ini ialah ", max(AEB,PRD,Psikin), ", yaitu pada matkul Psikologi Industri")
print ("Nilai terendah yang Fadhila dapatkan pada semester ini ialah ", min(AEB,PRD,Psikin), ", yaitu pada matkul AEB")

# Fungsi sqrt
import math
Alas = 30
Tinggi = 40
S_miring = Alas ** 2 + Tinggi ** 2
print ("Fadhila ditugaskan oleh Pak Hari untuk menghitung sisi miring lapangan fakultas teknik, \nhasil pengamatannya menyatakan bahwa besar sisi miring lapangan fakultas teknik ialah",math.sqrt(S_miring),"m")
